#include <unistd.h>

void	ft_putnbr(int nb)
{
	char aux;

	if (nb == -2147483648)
	{
		write(1, "-2", 2);
		nb = 147483648;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb <= 9)
	{
		aux = nb + '0';
		write(1, &aux, 1);
	}
	else
	{
		aux = (nb % 10) + '0';
		nb = nb / 10;
		ft_putnbr(nb);
		write(1, &aux, 1);
	}
}
